describe '四則演算' do
  context '足し算' do
    it '1 + 1は2になる' do
      expect(1 + 1).to eq 2
    end
  end
  context '足し算' do
    it '3 + 2は5になる' do
      expect(3 + 2).to eq 5
    end
  end
  context '引き算' do
    it '3-1は2になる' do
      expect(3-1).to eq 2
    end
  end
  context '引き算' do
    it'5-2は3になる' do
      expect(5-2).to eq 3
    end
  end
end

